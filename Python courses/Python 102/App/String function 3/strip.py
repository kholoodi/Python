text = '            Python Course            '
#strip() to remove speces and line  in start and end of string
print(text.strip())

#lstrip() to emove spece in the start of string
print(text.lstrip())

#rstrip() to emove spece in the end of string
print(text.rstrip())

# strip() remove /n a new line 
text ='\n Python Course'
print(text.strip())